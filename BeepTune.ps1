.Synopsis
   BeepTune
.DESCRIPTION
   Beep Tune JukeBox
.EXAMPLE
   BeepTune
   Play default tune (Vador)
.EXAMPLE
   BeepTune Mario
   Play Mario Tune
.EXAMPLE
   BeepTune Vladimir -Loop True
   Play Tetris tune in endless loop
   (ctrl-c to stop)
.EXAMPLE
   BeepTune -Loop True -Sleep 3600
   Play default tune every hour  
#>
